$sql = [
    // Tabla comisionistas
    "CREATE TABLE IF NOT EXISTS comisionistas (
        id_comision INT AUTO_INCREMENT PRIMARY KEY,
        nombre VARCHAR(100) NOT NULL,
        tipo VARCHAR(50) NOT NULL
    ) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;",

    // Tabla com_cliente
    "CREATE TABLE IF NOT EXISTS com_cliente (
        id_comision INT NOT NULL,
        id_cliente INT NOT NULL,
        porcentaje FLOAT(5,2) NOT NULL DEFAULT 0.00,
        PRIMARY KEY (id_comision, id_cliente),
        CONSTRAINT fk_com_cliente_comision FOREIGN KEY (id_comision) REFERENCES comisionistas(id_comision),
        CONSTRAINT fk_com_cliente_cliente FOREIGN KEY (id_cliente) REFERENCES sys_clientes(id_cliente)
    ) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;",

    // Tabla cat_grupos
    "CREATE TABLE IF NOT EXISTS cat_grupos (
        clave VARCHAR(10) PRIMARY KEY,
        grupo VARCHAR(150) NOT NULL
    ) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;",

    // Tabla cat_productos
    "CREATE TABLE IF NOT EXISTS cat_productos (
        clave VARCHAR(8) PRIMARY KEY,
        descripcion VARCHAR(255) NOT NULL,
        grupo VARCHAR(20),
        CONSTRAINT fk_productos_grupo FOREIGN KEY (grupo) REFERENCES cat_grupos(clave)
    ) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;",

    // Tabla cat_productos_clientes
    "CREATE TABLE IF NOT EXISTS cat_productos_clientes (
        id_cliente INT NOT NULL,
        clave VARCHAR(10) NOT NULL,
        PRIMARY KEY (id_cliente, clave),
        CONSTRAINT fk_productos_cliente_cliente FOREIGN KEY (id_cliente) REFERENCES sys_clientes(id_cliente),
        CONSTRAINT fk_productos_cliente_clave FOREIGN KEY (clave) REFERENCES cat_productos(clave)
